  //Q.2 Write a Program to print the below pattern using nested for loop.

#include<stdio.h>
   int main(){

    
    for(int i=1;i<=1;i++){
        for (int j=5;j<=1;j--)
 
        {
            if (j<=i)
            {
                printf("%d ",j);
                
            }
            
        }
        printf("\n");


    }


    return 0;
   }